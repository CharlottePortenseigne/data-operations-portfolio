WHERE event_type = 'purchase'
GROUP BY brand_clean
ORDER BY revenue DESC
LIMIT 20;

-- ============================================
-- 31. Category-level purchase summary
-- Prompt: Which categories generate the most purchases and revenue?
-- Why: Explore category-level performance while accounting for missing category_code values.
-- ============================================

SELECT 
	CAST
		WHEN category_code IS NULL OR category_code = '' THEN 'Unknown'
		ELSE category_code
	END AS category_clean, 
	COUNT(*) AS purchases,
	ROUND(SUM(price(price AS REAL)), 2) AS revenue,
	ROUND(AVG(CAST(price AS REAL)), 2) ASavg_price
FROM ecommerce
WHERE event_type = 'purchase'
GROUP BY category_clean
ORDER BY revenue DESC;

-- ============================================
-- 32. Monthly revenue trend
-- Prompt: How does revenue change over time?
-- Why: Identify monthly revenue patterns and seasonality.
-- ============================================

SELECT 
	SUBSTR(REPLACE(event_time, ' UTC',''), 1, 7,) AS purchase_month,
	COUNT(*) AS purchases,
	ROUND(SUM(CAST(price AS REAL)), 2) AS revenue
FROM ecommerce
WHERE event_type = 'purchase'
GROUP BY purchase_month
ORDER purchase_month;

-- ============================================
-- 33. Monthly purchasing users
-- Prompt: How many purchasing users are active each month?
-- Why: Compare customer activity, purchase volume, and revenue over time.
-- ============================================

SELECT 
	SUBSTR(REPLACE(event_time, ' UTC', ''), 1, 7) AS purchase_month,
	COUNT(DISTINCT user_id) AS purchasing_users,
	COUNT(*) AS purchases,
	ROUND(SUM(CAST(price AS REAL)), 2) AS revenue
FROM ecommerce
WHERE event_type = 'purchase'
GROUP BY purchase_month
ORDER BY purchase_month;

-- ============================================
-- 34. Final executive summary table
-- Prompt: What are the main project KPIs in one table?
-- Why: Provide a concise summary for the README and portfolio presentation.
-- ============================================

SELECT 
	COUNT(DISTINCT user_id) AS total_purchasing_users, 
	COUNT(*) AS total_purchase_events,
	ROUND(SUM(CAST(price AS REAL)), 2) AS total_revenue, 
	ROUND(AVG(CAST(price AS REAL)), 2) AS avg_purchase_value
FROM ecommerce
WHERE event_type = 'purchase';

-- ============================================
-- 35. Purchase value distribution
-- Prompt: How are purchases distributed across price ranges?
-- Why: Explain the difference between average and median purchase value and identify low-ticket vs higher-ticket revenue behavior.
-- ============================================

SELECT
	CASE
		WHEN CAST(price AS REAL) < 2 THEN 'Very Low (< $2)'
		WHEN CAST(price AS REAL) < 5 THEN 'Low ($2-$5)'
		WHEN CAST(price AS REAL) <10 THEN 'Medium ($5-$10)'
		ELSE 'High (> $10)'
	END AS price_bucket,
	COUNT(*) AS purchases,
	ROUND(SUM(CAST(price AS REAL)), 2) AS revenue,
	ROUND(AVG(CAST(price AS REAL)), 2) AS avg_price
FROM ecommerce
WHERE event_type = 'purchase'
GROUP BY price_bucket
ORDER BY revenue DESC;

-- ============================================
-- 36. Customer purchase span
-- Prompt: How long do repeat customers remain active?
-- Why: Measure engagement through first and last purchase dates and support customer lifecycle analysis.
-- ============================================

SELECT 
	user_id,
	MIN(event_time) AS first_purchase_date,
	MAX(event_time) AS last_purchase_date,
	COUNT(*) AS total_purchases,
	ROUND(SUM(CAST(price AS REAL)), 2) AS total_spent
FROM ecommerce
WHERE event_type = 'purchase'
GROUP BY user_id
HAVING COUNT(*) >= 2
ORDER BY total_purchases DESC, total_spent DESC;

